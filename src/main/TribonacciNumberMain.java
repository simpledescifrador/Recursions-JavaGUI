    private void computeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_computeActionPerformed
        int noTerms = Integer.parseInt(noTermField.getText());
        String ans = "", sol = "";
        arrayValues[0] = initVal0;
        arrayValues[1] = initVal1;
        arrayValues[2] = initVal2;
        for (int i = 3; i <= noTerms; i++) {
            //fibonacci formula
            arrayValues[i] = arrayValues[i-2] + arrayValues[i-1] + arrayValues[i-3];
        }
        //set the value to gui
        for (int i = 0; i <= noTerms; i++) {
            ans += String.valueOf(arrayValues[i]);
            sol += "T[" + i + "] = " + String.valueOf(arrayValues[i]);
            if(i < noTerms) {
                ans += ", ";
                sol += ", ";
            }else {
                ans += " for 0<=n<=" + noTerms;
            }

        }
        if(noTerms < 3){
            alertMsg.setText("Invalid no of terms!!");
            answer.setText("");
            solution.setText("");
        }else{
            solution.setText(sol);
            answer.setText(ans);
            alertMsg.setText("");
        }
        noTermField.setText("");
    }//GEN-LAST:event_computeActionPerformed

